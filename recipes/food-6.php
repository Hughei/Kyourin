                                                    <h3> Dorayaki (Japanese Red Bean Pancake)</h3>
                                                    <h4>Doraemon</h4>
                                                    <h5>By: Just One Cookbook</h5>
                                                    <div class="price">
                                                        <strong class="current">
                                                            Best described as a dessert with red bean filling between two slices of sweet fluffy pancakes. 
                                                            If you are familiar with Japanese cartoons from the ’70s, you probably know this dessert from the 
                                                            anime character Doraemon who is crazy about this snack and falls for any trap involving them.</strong>

                                                        <div class="ing">                                                        
                                                            
                                                            <ul>
                                                                <strong class="ingBold">Ingredients:</strong>
                                                                <li>• 4 large eggs</li>
                                                                <li>• 140 g sugar (⅔ cup + ½ tbsp)</li>                                                                
                                                                <li>• 5 tbsp honey</li>
                                                                <li>• 1 ⅓ cup all purpose flour (plain flour)</li>
                                                                <li>• 1 tsp baking powder</li>
                                                                <li>• 1 - 2  tbsp water</li>
                                                                <li>• Neutral oil</li>
                                                                <li>• 1.1 lb sweet red bean paste (anko)</li> 
                                                                <li>&nbsp</li> 
                                                                <strong class="ingBold">Directions:</strong>
                                                                <li><u class="ingUnder">To Make the Batter</u></li>
                                                                <li>1. In a large bowl, combine 4 large eggs, 140 g sugar (⅔ cup + ½ Tbsp), and 2 tbsp honey. 
                                                                    Whisk well until the mixture becomes fluffy.</li>
                                                                <li>2. Sift 1⅓ cup all-purpose flour (plain flour) and 1 tsp baking powder into the bowl with the egg mixture and mix until combined. 
                                                                    Put the bowl in the refrigerator to rest the batter for 15 minutes.</li>
                                                                <li>3. After resting, the batter should be relaxed and slightly smoother. Now, stir in half of the 1–2 Tbsp water and check the consistency. It should be similar to pancake batter. Add more of the water, as needed, until you achieve the right consistency. 
                                                                    Depending on the size of the eggs and how accurate your flour measurement is, the water amount may vary.</li>                                                              
                                                            </ul> 
                                                            <ul>                                                              
                                                                <li><u class="ingUnder">To Cook the Honey Pancakes</u></li>
                                                                <li>1. Heat a large nonstick frying pan over the lowest heat setting for 5 minutes. 
                                                                    It‘s best to take your time and heat the pan slowly. When it is thoroughly heated with no hot spots, 
                                                                    increase the heat to medium low. Dip a paper towel in neutral oil and coat the bottom of the pan with oil. 
                                                                    Then, use another paper towel to remove the oil completely. This is the key to evenly golden brown Dorayaki pancakes. 
                                                                    With a ladle or a small measuring cup, pour 3 Tbsp of the batter from 3 inches above the pan to create a pancake that‘s 3 
                                                                    inches in diameter. Cook one pancake at a time.</li>
                                                                <li>2. When you see the surface of the batter starting to bubble, flip the pancake over and cook the other side. 
                                                                    (With my stove and frying pan, it takes 1 minute and 15–30 seconds to cook one side and 20–30 seconds for the other side.) When done, transfer it to a plate and cover it with a damp towel to prevent it from drying out. Grease the pan between batches, as needed. 
                                                                    Continue making the rest of the pancakes; you can make about 12 pancakes.</li>
                                                                    <li>&nbsp</li> 
                                                                <li><u class="ingUnder">To Assemble the Dorayaki</u></li>
                                                                <li>1. Assemble the Dorayaki by making sandwiches using two pancakes and a scoop of the 1.1 lb sweet red bean paste (anko) as filling. 
                                                                        Put more red bean paste in the center of the pancakes so the sandwich‘s middle is thicker than the edges and the Dorayaki's overall shape is curved. 
                                                                        Wrap the Dorayaki with plastic wrap until ready to serve.</li>                                                                                                           
