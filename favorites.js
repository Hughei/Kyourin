document.addEventListener('DOMContentLoaded', () => {
    const isToggled = localStorage.getItem('isToggled1') === 'true';
    const items = document.querySelectorAll('.containerProds1');
    

    if (isToggled) {
        items.forEach((item) => {
            item.style.display = 'none'; // Hide items            
        });
    }
    
});


